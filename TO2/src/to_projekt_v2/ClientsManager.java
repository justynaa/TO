    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ID = jTextFieldID.getText();
        if (savedID.equals(ID)) {
            try {
                client = new Client(Integer.parseInt(jTextFieldID.getText()), jTextFieldName.getText(), jTextFieldSurname.getText(), jTextFieldAddress.getText(), jTextFieldPhone.getText(), jTextFieldEmail.getText() );
                //Integer.parseInt() v3_6 zmiana String na Int do konstruktora Client
                clientDAO.updateClientDataDAO(client);
                //odswiezenie danych widocznych w JTable
                DefaultTableModel model = (DefaultTableModel)jTableDisplayUsers.getModel();
                model.setRowCount(0);
                showClientsInJTable();
            } catch (SQLException ex) {
                Logger.getLogger(UsersManager.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Nie można zmienić ID klienta!");
        }
   
    }//GEN-LAST:event_jButton2ActionPerformed

