    public void showClientsInJTable() throws SQLException{
        ArrayList<Client> clientsList = clientDAO.getClientsList();
        DefaultTableModel model = (DefaultTableModel)jTableDisplayUsers.getModel();
        model.setRowCount(0);        
        Object[] row = new Object[6];       //6 kolumn
        for(int i=0; i < clientsList.size(); i++){
            row[0] = clientsList.get(i).getID();
            row[1] = clientsList.get(i).getName();
            row[2] = clientsList.get(i).getSurname();
            row[3] = clientsList.get(i).getAddress();
            row[4] = clientsList.get(i).getPhone();
            row[5] = clientsList.get(i).getEmail();
            model.addRow(row);
        }
        
    }
    public void showChosenClientsInJTable(Client client) throws SQLException{
        if (jTextFieldID.getText().equals("")){
            client = new Client(0, jTextFieldName.getText(), jTextFieldSurname.getText(), jTextFieldAddress.getText(), jTextFieldPhone.getText(), jTextFieldEmail.getText());
        }
        else {
            int id = Integer.parseInt(jTextFieldID.getText());
            client = new Client(id, jTextFieldName.getText(), jTextFieldSurname.getText(), jTextFieldAddress.getText(), jTextFieldPhone.getText(), jTextFieldEmail.getText());
        }
        ArrayList<Client> chosenClientsList = clientsBrowser.getChosenClientsList(client);
        DefaultTableModel model = (DefaultTableModel)jTableDisplayUsers.getModel();
        model.setRowCount(0);
        
        Object[] row = new Object[6];       //6 kolumn
        for(int i=0; i < chosenClientsList.size(); i++){
            row[0] = chosenClientsList.get(i).getID();
            row[1] = chosenClientsList.get(i).getName();
            row[2] = chosenClientsList.get(i).getSurname();
            row[3] = chosenClientsList.get(i).getAddress();
            row[4] = chosenClientsList.get(i).getPhone();
            row[5] = chosenClientsList.get(i).getEmail();
            model.addRow(row);
        }
        
    }

    
